     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $users = User::all ();
        $tmp = array();

        foreach ($users as $user) {
            array_push ($tmp, array(
                'id' => $user->id,
                'name' => $user->name,
                'email' => $user->email,
                'role_id' => $user->role_id,
                'role' => $user->getRole->name,
            ));
        }
        return response ()->json ($tmp);
    }

